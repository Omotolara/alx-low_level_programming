#include "stdio.h"

/**
 * add: performs addition operation
 * @a: first number
 * @b: second number
 * Return: the addition
 */
int add(int a, int b) {
	return (a + b);
}

/**
 * sub: performs subtraction operation
 * @a: first number
 * @b: second number
 * Return: the result
 */
int sub(int a, int b) {
	return (a - b);
}

/**
 * mul: performs multiplication operation
 * @a: first number
 * @b: second number
 * Return: the result
 */
int mul(int a, int b) {
	return (a * b);
}

/**
 * div: performs division operation
 * @a: first number
 * @b: second number
 * Return: the result
 */
int div(int a, int b) {
	return (a / b);
}

/**
 * mod: performs modulo operation
 * @a: first number
 * @b: second number
 * Return: the result
 */
int mod(int a, int b) {
	return (a % b);
}
